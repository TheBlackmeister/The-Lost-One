package cz.cvut.fel.pjv.Model.Utils;

public class Tuple {
    private int first;
    private int second;

    public Tuple(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
